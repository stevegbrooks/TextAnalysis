/**
 * This class creates a Quote object which contains 
 * a Quote from a book as a String.
 * 
 * @author sgb
 *
 */
public class Quote {
	private String quote;
	
	public Quote(String quote) {
		this.quote = quote;
	}

	/**
	 * @return the quote
	 */
	public String getQuote() {
		return quote;
	}

}
